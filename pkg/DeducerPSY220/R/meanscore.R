# Computing the mean of several items

meanscore <- function(data.frame, items)
 {
 sub <- data.frame[items]
 avg <- apply(sub,1,mean)
 avg
 }
# items needs to be a vector of column names, defined previously using the c 
# function.
# Problem: any missing value produces a "NA" value for that participant.

